function addDefaultMaterial(a, b, c, d, e, f, g, h) {
  a.forEach((elm) => {
    elm.material = f;
  });
  b.forEach((elm) => {
    elm.material = g;
  });
  c.forEach((elm) => {
    elm.material = g;
  });
  d.forEach((elm) => {
    elm.material = g;
  });
  e.forEach((elm) => {
    elm.material = h;
  });
}
